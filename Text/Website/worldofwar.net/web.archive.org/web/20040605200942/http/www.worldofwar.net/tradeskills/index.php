                              <p>&nbsp;<font face="Verdana" size="2" color="#DCDFE4">Trade
                              skills allow you to forge a profession for
                              yourself in the game. They allow for greater customization within the
                              classes but are not necessary in order to the play
                              the game but are another aspect to role playing
                              within the world.&nbsp; You will also see them
                              referred to as &quot;Crafting
                              Skills&quot; sometimes.</font></p>

                              <p><font face="Verdana" size="2" color="#DCDFE4">They
                              are taught by Trainers.&nbsp; Teachers dotted around
                              the cities and towns who in exchange for skill
                              points will teach you their profession.&nbsp; Each
                              trainer teaches in one area only. You won't find a
                              Trainer who can train in Alchemy and Tailoring and
                              Engineering.&nbsp; You can use our maps in the <a href="https://web.archive.org/web/20040605201548/http://www.worldofwar.net/cartography">cartography</a>
                              section to locate Trainers in the city.&nbsp; Most
                              if not all the Trade skills come with
                              supportive skills such as Herbalism having 'Find
                              Herbs' to help you locate plants in the wilderness
                              or 'Find Ore' coming with Mining.</font></p>

                              <font face="Verdana" size="2" color="#DCDFE4">

                              <p>To recap on some essential points:&nbsp;</p>

                              <ul>
                                <li>Cost
                                  Skill Points to learn each Trade Skill, <b>not</b>
                                  money</li>
                                <li>Learned
                                  at specialized Trainers {teachers} dotted
                                  around towns and cities</li>
                                <li>Can train to three levels of proficiency Apprentice, Journeyman and Expert</li>
                                <li>Trade Skills are not class specific</li>
                                <li>Recipes to use with the Skill cost money</li>
                                <li>Reagents {ingredients} can be found with
                                  gathering Skills, traded from other players
                                  and in some cases bought from merchants</li>
                                <li>The more you use your chosen Trade Skill
                                  the more your proficiency level in it rises</li>
                                <li>The higher you rise in proficiency the more
                                  products/potions you can make</li>
                                <li>You can unlearn your Trade Skills. So if
                                  you began to train as a Tailor and then decide
                                  you would rather be a cook, you can unlearn as
                                  Tailor and regain your Skill Points to spend
                                  in Cooking.&nbsp; You can do that multiple
                                  times.</li>
                              </ul>
